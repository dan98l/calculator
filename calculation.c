#include <stdio.h>

double addition(double S, double l){
    double summ = 0;
    summ = S + l;
    
    return summ;
}

double subtraction(double S, double l){
    double minm = 0;
    minm = S - l;
    
    return minm;
}

double multiplication(double S, double l){
    double pro = 0;
    pro = S * l;
    
    return pro;
}

double division(double S, double l){
    double div = 0;
    div = S / l;
    
    return div;
}
