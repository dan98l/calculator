#include <stdio.h>

int addition(int S, int l)
{
    int summ = 0;
    summ = S + l;
    
    return summ;
}

int subtraction(int S, int l)
{
    int minm = 0;
    minm = S - l;
    
    return minm;
}

int multiplication(int S, int l)
{
    int pro = 0;
    pro = S * l;
    
    return pro;
}

int division(int S, int l)
{
    int div = 0;
    div = S / l;
    
    return div;
}
